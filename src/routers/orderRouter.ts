import { Router } from "express";
import {
  getOrders,
  createOrder,
  getOrderById,
  deleteOrder
} from "#controllers";

const orderRouter = Router();

orderRouter.route("/")
  .get(getOrders)
  .post(createOrder);

orderRouter.route("/:id")
  .get(getOrderById)
  .delete(deleteOrder);

export default orderRouter;

/**
 * @swagger
 * tags:
 *   name: Orders
 *   description: Order management endpoints
 */

